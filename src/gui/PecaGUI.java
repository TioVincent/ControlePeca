    private void bCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bCreateActionPerformed
        int codigo = Integer.parseInt(tfCodigoCreate.getText());
        String nome = tfNomeCreate.getText();
        Double preco = Double.parseDouble(tfPreco.getText());

        int indiceFornecedor = this.jComboBoxFornecedores.getSelectedIndex();
        Fornecedor fornecedor = this.fornecedores.get(indiceFornecedor);

        try {

            Peca peca = new Peca(codigo, nome, preco, fornecedor.getCodigo());
            daoPeca.insere(peca);
        } catch (SQLException exc) {
            exc.printStackTrace();
        }

        tfCodigoCreate.setText("");
        tfNomeCreate.setText("");
        tfPreco.setText("");
        jComboBoxFornecedores.setSelectedIndex(-1);
    }//GEN-LAST:event_bCreateActionPerformed

    private void bAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAtualizarActionPerformed

        int codigo = Integer.parseInt(tfCodigoAtualizadoUpdate.getText());
        String nome = tfNomeUpdate.getText();
        Double preco = Double.parseDouble(tfPreco1.getText());

        int indiceFornecedor = this.jComboBox1.getSelectedIndex();
        Fornecedor fornecedor = this.fornecedores.get(indiceFornecedor);

        int codigoNovoFornecedor = fornecedor.getCodigo();

        Peca peca = new Peca(codigo, nome, preco, codigoNovoFornecedor);

        try {
            daoPeca.atualiza(peca);

            this.tfCodigoAtualizadoUpdate.setText("");
            this.tfNomeUpdate.setText("");
            this.tfPreco1.setText("");
            this.jComboBox1.setSelectedIndex(-1);
            this.tfCodigoAtualizadoUpdate.setText("");

        } catch (SQLException exc) {
            exc.printStackTrace();
        }
    }//GEN-LAST:event_bAtualizarActionPerformed

