    private void bRetrieveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bRetrieveActionPerformed

        String termoBuscadoCodigo = tfPesquisaCod.getText();
        String termoBuscadoNome = tfPesquisaPeca.getText();

        try {

            List<Peca> pecas = daoPeca.consultaPorNomeOuCodigo(termoBuscadoCodigo, termoBuscadoNome);

            PesquisaTableModel model = (PesquisaTableModel) jTablePecas.getModel();

            model.setPecas(pecas);
            model.fireTableDataChanged();

        } catch (SQLException exc) {
            exc.printStackTrace();
        }
    }//GEN-LAST:event_bRetrieveActionPerformed

    private void linhaClicada(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_linhaClicada

        int clickCount = evt.getClickCount();
        if (clickCount == 2) {
            PesquisaTableModel model = (PesquisaTableModel) jTablePecas.getModel();
            List<Peca> pecas = model.getPecas();

            int row = jTablePecas.getSelectedRow();

            Peca peca = pecas.get(row);

            this.TabbedPane.setSelectedIndex(2);
            this.tfCodigoAtualizadoUpdate.setText(peca.getCodigo() + "");

            buscaPecaEAtualiza();
        }

    }//GEN-LAST:event_linhaClicada

