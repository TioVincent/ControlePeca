    private void iniciarFornecedores() {

        try {
            fornecedores = daoFornecedor.lista();
            int qtdFornecedores = fornecedores.size();
            String[] fornecedoresLista = new String[qtdFornecedores];

            for (int i = 0; i < qtdFornecedores; i++) {
                Fornecedor f = fornecedores.get(i);
                fornecedoresLista[i] = f.getNome() + " - " + f.getCidade();
            }

            jComboBoxFornecedores.setModel(new DefaultComboBoxModel(fornecedoresLista));
            jComboBox1.setModel(new DefaultComboBoxModel(fornecedoresLista));
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

    private int verificaIndice(int codigoFornecedor) {
        int result = -1;

        if (fornecedores != null) {
            for (int i = 0; i < fornecedores.size(); i++) {
                Fornecedor t = fornecedores.get(i);
                if (t.getCodigo() == codigoFornecedor) {
                    result = i;
                    break;
                }
            }
        }

        return result;
    }

