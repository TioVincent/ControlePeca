    private void buscaPecaEAtualiza() {
        int codigo = Integer.parseInt(tfCodigoAtualizadoUpdate.getText());

        try {
            peca = daoPeca.consulta(codigo);

            tfCodigoAtualizadoUpdate.setText(((Integer) peca.getCodigo()).toString());
            tfNomeUpdate.setText(peca.getNome());
            tfPreco1.setText(Double.toString(peca.getPreco()));

            int indice = verificaIndice(peca.getFornecedor());
            jComboBox1.setSelectedIndex(indice);
        } catch (SQLException exc) {
            exc.printStackTrace();
        }
    }
